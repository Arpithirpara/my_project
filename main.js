function renderProducts(panelId, products) {
  const panel = document.getElementById(panelId);
  panel.innerHTML = ""; // optional: clear old data

  products.forEach((item) => {
    panel.innerHTML += `
      <div class="p_item">
        <img src="${item.img}">
        <p>8 mins</p>
        <h4>${item.Name}</h4>
        <p>${item.weight}</p>
        <p class="price">₹${item.price}</p>

        <button class="btn" onclick="showQty(this)">ADD</button>

        <div class="increaseQty" style="display:none">
          <button class="increment" onclick="decrement(this)">-</button>
          <span class="qty">0</span>
          <button class="decrement" onclick="increment(this)">+</button>
        </div>
      </div>
    `;
  });
}


function addToCart(name, price, qty){
    let cart = JSON.parse(localStorage.getItem("cart")) || {};

    cart[name] = {
        price: price,
        qty: qty
    };

    localStorage.setItem("cart", JSON.stringify(cart));
}

function removeFromCart(name){
    let cart = JSON.parse(localStorage.getItem("cart")) || {};
    delete cart[name];
    localStorage.setItem("cart", JSON.stringify(cart));
}

function showQty(btn){
    btn.style.display="none";  
   
    let qtyBox = btn.nextElementSibling;
    qtyBox.style.display="flex"; 
    qtyBox.querySelector('.qty').textContent="1";

    let product = btn.closest(".p_item");
    let name = product.querySelector("h4").textContent;
    let price = parseInt(product.querySelector(".price").textContent.replace("₹",""));

    addToCart(name, price, 1);

    updateCartColor();
}

function increment(btn){
    let span = btn.previousElementSibling;
    let qty = parseInt(span.textContent) + 1;
    span.textContent = qty;

    let product = btn.closest(".p_item");
    let name = product.querySelector("h4").textContent;
    let price = parseInt(product.querySelector(".price").textContent.replace("₹",""));

    addToCart(name, price, qty);

    updateCartColor();
}

function decrement(btn){
    let span = btn.nextElementSibling;
    let qty = parseInt(span.textContent) - 1;

    let product = btn.closest(".p_item");
    let name = product.querySelector("h4").textContent;
    let price = parseInt(product.querySelector(".price").textContent.replace("₹",""));

    if(qty > 0){
        span.textContent = qty;
        addToCart(name, price, qty);
    } else {
        span.textContent = "0";
        btn.parentElement.style.display="none";
        btn.parentElement.previousElementSibling.style.display="block";
        removeFromCart(name);
    }

    updateCartColor();
}

  



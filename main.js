  function updateCartColor() {
  let totalQty = 0;
  let allQty = document.querySelectorAll(".qty");

  allQty.forEach(span => {
    totalQty += parseInt(span.textContent);
  });

  let cart = document.getElementById("my_cart");
  if (totalQty > 0) {
    cart.style.background = " green";
  } else {
    cart.style.background = "";
  }
}

function loadCartUI() {
    let cart = JSON.parse(localStorage.getItem("cart")) || {};

    document.querySelectorAll(".p_item").forEach(product => {
        let name = product.querySelector("h4").textContent;
        let addBtn = product.querySelector(".btn");
        let qtyBox = product.querySelector(".increaseQty");
        let qtySpan = product.querySelector(".qty");

        if (cart[name]) {
            // Item cart me hai → show qty box
            addBtn.style.display = "none";
            qtyBox.style.display = "flex";
            qtySpan.textContent = cart[name].qty;
        } else {
            // Item cart me nahi → reset UI
            addBtn.style.display = "block";
            qtyBox.style.display = "none";
            qtySpan.textContent = "0";
        }
    });

    updateCartColor();
}
